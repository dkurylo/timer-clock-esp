std::vector<uint8_t> TCFonts::getSymbol( uint8_t fontIndex, char symbol, bool isBold, bool isWide, bool isProgress, bool isSmall ) {
  if( !TCFonts::isFontInitialised || TCFonts::fontIndex != fontIndex ) {
    TCFonts::isFontInitialised = true;
    TCFonts::fontIndex = fontIndex;
    if( fontIndex == 0 ) {
      TCFonts::initFont0();
    } else if( fontIndex == 1 ) {
      TCFonts::initFont1();
    } else if( fontIndex == 2 ) {
      TCFonts::initFont2();
    } else {
      TCFonts::initFont0();
    }
  }

  String key;
  if( isSmall ) {
    //symbol - isProgress
    key = String( symbol ) + String( isProgress ? "1" : "0" );
  } else {
    //symbol - isProgress - isThin - isBold
    key = String( symbol ) + String( isProgress ? "1" : "0" ) + String( !isWide ? "1" : "0" ) + String( isBold ? "1" : "0" );
  }
  std::map<String, std::vector<uint8_t>>::iterator it = TCFonts::symbols.find( key );
  if( it == TCFonts::symbols.end() ) {
    return { 0, 0, 0, 0, 0, 0, 0, 0 };
  }
  return it->second;
}
